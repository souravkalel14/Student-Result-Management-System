    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollno=jTextField7.getText();
        String name=jTextField8.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fathername=jTextField9.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/studentmanagementsystem","root","iamsolemon14");
            
            if(rollno.isEmpty()){
                JOptionPane.showMessageDialog(null,"Roll number cannot be empty!");
            }
            else{
                pre=con.prepareStatement("insert into student(rollno,course,branch,name,gender,fathername)values(?,?,?,?,?,?)");
                pre.setString(1,rollno);
                pre.setString(2,course);
                pre.setString(3,branch);
                pre.setString(4,name);
                pre.setString(5,gender);
                pre.setString(6,fathername);
                pre.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Succesfully added new student");
                setVisible(false);
                new adminHome().setVisible(true);
            } 
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

